#include <iostream>

using namespace std;

int main() {
    // Ввод исходного массива строк
    cout << "Введите количество строк в исходном массиве: ";
    int n;
    cin >> n;

    string originalArray[n];
    cout << "Введите строки исходного массива:" << endl;
    for (int i = 0; i < n; i++) {
        cin >> originalArray[i];
    }

    // Создание нового массива для строк длиной не более 3 символов
    int newSize = 0;
    for (int i = 0; i < n; i++) {
        if (originalArray[i].length() <= 3) {
            newSize++;
        }
    }

    string newArray[newSize];

    // Заполнение нового массива
    int index = 0;
    for (int i = 0; i < n; i++) {
        if (originalArray[i].length() <= 3) {
            newArray[index] = originalArray[i];
            index++;
        }
    }

    // Печать нового массива
    cout << "Новый массив строк длиной не более 3 символов:" << endl;
    for (int i = 0; i < newSize; i++) {
        cout << newArray[i] << endl;
    }

    return 0;
}

//Ввод исходного массива строк: Пользователь вводит количество строк в исходном массиве и сами строки.

//Создание нового массива для строк длиной не более 3 символов: Определяется размер нового массива, подсчитывая количество строк в исходном массиве, длина которых не превышает 3 символов.

//Заполнение нового массива: Новый массив заполняется строками из исходного массива, длина которых не превышает 3 символов.

//Новый массив выводится на экран.